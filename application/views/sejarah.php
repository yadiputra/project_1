                <p>History Pondok Pesantren Darussalamah yang bertempat di Jl. Tapak Libok, Rt. 002 Rw. 001, Desa Marga Sakti, Kecamatan Muara Kelingi, Kabupaten Musi Rawas Provinsi Sumatera Selatan. Yang dipimpin dan diasuh oleh KIAI KEMBAR.</p>

				<p>Kali ini kami akan sampaikan historis sejarah berdirinya Pondok Pesantren Darussalamah, bermula adanya seorang tokoh masyarakat yang elektabilitasnya tidak diragukan lagi, beliau adalah H. Muhammad Jimin Wardiyanto Alm. dan juga didukung oleh tokoh yang lain antara lain adalah Kyai Shobari/Munanto, Bapak Cucuk Suryalana, Bapak Wijono dan Bapak Ansori Alm.</p>

				<p>Pada awal mula dirintis pada tahun 1997 dengan berdirinya sebuah lembaga Taman Pendidikan Al-Qur&rsquo;an (TPA), yang pada saat itu dipimpin oleh Ky. M. Shobari. Kemudian setelah H. Jimin Wardiyanto dan Kyai Shobari bertemu dengan Ky. Ustman Syafi&rsquo;i, (tepatnya saat trans / pindahan dari Lampung). Kyai Shobari berharap Ky. Ustman Syafi&rsquo;i dapat meneruskan perjuangan-Nya untuk menyiarkan Ajaran Agama Islam. Dan H. Jimin Wardiyanto Alm. juga berpesan berharap nantinya Ky. Usman Syafi&#39;i dapat menjadi Pimpinan dan sekaligus Pengasuh Pondok Pesantren tersebut.</p>

				<p>Kemudian pada tanggal 18 November 1999 dilaksanakan peletakan batu pertama untuk perubahan menjadi Lembaga Pendidikan Pondok Pesantren dengan dinamai Pondok Pesantren Darussalamah, pada saat itu dihadiri langsung oleh Syekh Ahmad Shodiq Alm.</p>

				<p>Pemberian nama Pesantren tersebut bermula dari referensi tabarukan Ky. Ustman Syafi&rsquo;i kepada Syekh Ahmad Shodiq Alm. selaku guru beliau, pengasuh, pimpinan sekaligus pendiri Pondok Pesantren Darussalamah Way Jepara Lampung Tengah dan juga menjadi sebuah kebetulan H. Jimin Wardiyanto Alm. ini mempunyai seorang anak yang ke-2 dengan nama Salamah, maka setelah melaksanakan musyawarah dan mufakat bersama, hingga akhirnya ditetapkanlah sebuah nama DARUSSALAMAH.</p>

				<p>Dengan fasilitas yang telah disediakan dan beberapa hektar lahan yang telah diwakafkan, Ky. Ustman Syafi&rsquo;i didukung penuh untuk mengajarkan ilmu agama dan menciptakan generasi yang berpendidikan agama secara luas.</p>

				<p>Waktu demi waktu terus berjalan, Ky. Ustman Syafi&rsquo;i pernah mengungkapkan bahwa Pondok Pesantren Darussalamah ini ternyata kurang diminati oleh masyarakat, untuk menangani masalah tersebut maka beliau selalu melakukan evaluasi dan pembaruan dengan tujuan menyesuaikan perkembangan dan tuntutan masyarakat.</p>

				<p>Pada mulanya dulu Pondok Pesantren Darussalamah hanya mempunyai Madrasah Diniyah Takmiliyah Awwaliyah dan Madrasah Diniyah Takmiliyah Wustho, selanjutnya pada tahun 2014 kemudian mengembangkan sayapnya dengan mendirikan Madrasah dengan adanya pendidikan Formal, barulah pada saat itu berdiri Raudlatul Athfal (RA Muslimat NU), karena Pondok Pesantren ini Notaben dalam ke-organisasian nya adalah Nahdlatul &lsquo;Ulama. Kemudian pada tahun 2015 disusul didirikanya Madrasah Tsanawiyah (MTs Ma&#39;arif NU).</p>

				<p>Alhamdulillah pada tahun 2017 bisa menyusul mendirikan Madrasah Aliyah (MA Ma&rsquo;arif NU) selalu berkembang dan menyesuaikan tuntutan perkembangan zaman. Kemudian banyak para wali santri yang mendaftarkan putra-putrinya di sini, karena berharap ada tujuan pokok dunia dapat dan yang paling utama akhirat dapat.</p>

				<p>Walaupun Pondok Pesantren Darussalamah saat ini sudah mulai memasuki Pondok Modern, akan tetapi disini masih tetap dipertahankan dan selalu ditekankan akan ajaran Kitab Kuning-nya. Pembelajaran ini dimulai dari pemahaman Kitab Jurumiah, At-Tashrifiah, Durrotul Yatimah, Al-Imritim, Al- Fiah Ibnu Malik sampai kitab kelas tinggi seperti Kitab Kailani, Sulamut Taufiq, Fathul Qhorib, I&rsquo;anatutholibin, Mantiq, Balaghoh, Hikam, &lsquo;Uqudul Juman, Hisab, Falaq dan lain-lain.</p>

				<p>Pemahaman ilmu Tauhid disini adalah adalah menurut Manhaj Imam Al-Asy&#39;ari, Al-Maturidi kemudian fikirnya Mazhab Imam Syafi&#39;i yang paling prioritasnya dan Imam Hambali, Imam Hanafi dan Imam Maliki dan kemudian di bidang akhlak atau tasawuf yaitu Imam Al-Ghazali, Imam Al Junaidi Al Baghdadi.</p>
				   
